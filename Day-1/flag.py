import turtle

pen = turtle.Turtle()
pen.forward(20)
pen.forward(50)
pen.forward(50)
pen.right(90)  # Rotate clockwise by 90 degrees

pen.forward(50)
pen.right(90)

pen.forward(50)
pen.right(90)

pen.forward(50)
pen.forward(50)
pen.forward(50)
pen.forward(50)
pen.forward(50)
pen.right(90)

turtle.done()
